# Operadores Aritmétricos e Booleanos
# Operações Matematicas (Aritmétricas)
# Divisão Inteira descarta a parte decimal e considera a parte inteira
"""
 - Soma: +
 - Subtração: -
 - Multiplicação: *
 - Divisão: /
 - Divisão inteira: //
 - Resto da divisão: %
 - Potencia: **
"""

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 // 3)
print(20 % 3)
print(2 ** 3)

# Operadores Booleanos

idade1 = 10
idade2 = 15
altura1 = 1.77
altura2 = 1.65
altura3 = altura2

# > : Maior que
print(idade1 > idade2)
# <= : Menor igual a 
print(idade1 <= idade2)          
# == : Igual a 
print('Python' == 'python')
# != : Diferente de        
print('banana' != 'abacaxi')
# >= : Maior ou igual a       
print(altura1 >= altura2)
# > : Maior que
print(altura2 > altura3)         
# < : Menor que
print(altura3 < altura1)